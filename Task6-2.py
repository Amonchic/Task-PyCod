# Шаг 1: Создаём два списка фамилий
group1 = [
    "Петров", "Сидоров", "Козлов", "Васильев", "Морозов",
    "Белов", "Григорьев", "Лебедев", "Семёнов", "Тихонов"
]

group2 = [
    "Андреев", "Фёдоров", "Захаров", "Борисов", "Егоров",
    "Дмитриев", "Николаев", "Максимов", "Орлов", "Павлов"
]

# Шаг 2: Создаём спортивную команду — объединяем списки
sports_team = group1 + group2  # конкатенация списков

# Шаг 3: Выводим исходные списки и новый
print("Группа 1:", group1)
print("Группа 2:", group2)
print("\nСпортивная команда (объединённый список):")
print(sports_team)

# Шаг 4: Выводим длину команды
print(f"\nДлина команды: {len(sports_team)} человек")

# Шаг 5: Сортируем по алфавиту
sports_team.sort()
print("\nКоманда после сортировки по алфавиту:")
print(sports_team)

# Шаг 6: Ищем студента "Иванов"
ivanov_count = sports_team.count("Иванов")  # сколько раз встречается

if ivanov_count > 0:
    print(f"\nСтудент 'Иванов' встречается в команде {ivanov_count} раз(а).")
else:
    print("\nСтудент 'Иванов' не найден в команде.")