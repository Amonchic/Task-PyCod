# Шаг 1: Создаём словарь с осадками
rainfall = {
    "Январь": 45,
    "Февраль": 30,
    "Март": 60,
    "Апрель": 80,
    "Май": 120,
    "Июнь": 150,
    "Июль": 180,
    "Август": 160,
    "Сентябрь": 100,
    "Октябрь": 70,
    "Ноябрь": 50,
    "Декабрь": 40
}

print("Исходный словарь (месяц: осадки):")
for month, rain in rainfall.items():
    print(f"{month}: {rain} мм")

# Шаг 2: Сортируем по значениям (осадкам) — от меньшего к большему
sorted_rainfall = sorted(rainfall.items(), key=lambda x: x[1])

print("\nМесяцы, отсортированные по возрастанию осадков:")
for month, rain in sorted_rainfall:
    print(f"{month}: {rain} мм")