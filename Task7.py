        if data == "":  # –µ—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –Ω–∞–∂–∞–ª Enter –±–µ–∑ –≤–≤–æ–¥–∞
            print("–í–≤–æ–¥ –∑–∞–≤–µ—Ä—à—ë–Ω.")
